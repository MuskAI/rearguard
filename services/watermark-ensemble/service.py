@app.post("/v1/analyze")
def analyze_route():
    if not _authorized():
        return jsonify({"detail": "unauthorized"}), 401
    uploaded = request.files.get("file")
    if uploaded is None or not uploaded.filename:
        return jsonify({"detail": "file is required"}), 400
    data = uploaded.stream.read(MAX_UPLOAD_BYTES + 1)
    if not data:
        return jsonify({"detail": "empty file"}), 400
    if len(data) > MAX_UPLOAD_BYTES:
        return jsonify({"detail": "file too large"}), 413
    try:
        image = Image.open(__import__("io").BytesIO(data)).convert("RGB")
        candidates = json.loads(request.form.get("candidates", "[]"))
        registry_hits = json.loads(request.form.get("registryHits", "[]"))
        if not isinstance(candidates, list) or not isinstance(registry_hits, list):
            raise ValueError("candidate_schema_invalid")
        result = analyze(image, candidates, registry_hits)
        return {"status": "ok", "explicitWatermark": result, "visibleHits": result["hits"]}
    except Exception as exc:
        app.logger.exception("watermark ensemble failed")
        return jsonify({"detail": "ensemble failed", "errorType": type(exc).__name__}), 422


